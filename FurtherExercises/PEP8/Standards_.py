# Naming Conventions 

# Don't use: l O I for single letter var names.

# Use snake case for both package, function and variable names. e.g. my_function. Also use lowercase.

# for packages use short lowercase words without an underscore. mypackage

# for class names use camel case. MyFunction 

# Use capital letters for constants. BOLTZMANN or L 


# use variable names that link to the data that they have been assigned to. 


